class _MyHomePageState extends State<MyHomePage> {

  @override
  Widget build(BuildContext context) {
    var children = <Widget>[];
    // 生成 6 个 Tab 页
    for (int i = 0; i < 6; ++i) {
      children.add(Page2(text: '$i'));
    }
    return Scaffold(
        appBar: AppBar(
          title: Text("滚动组件")),

        body:PageView(
              scrollDirection: Axis.vertical, // 滑动方向为垂直方向
